// incrementing

console.log(1)
console.log(2)
console.log(3)
console.log(4)
console.log(5)
console.log('Counting completed!')

for (var i = 1; i <= 5; i++){
    console.log(i)
}
console.log('Counting completed!')


// decrementing

console.log(5)
console.log(4)
console.log(3)
console.log(2)
console.log(1)
console.log('Countdown finished!')

for(var i = 5; i >= 1; i--){
    console.log(i)
}
console.log('Countdown finished!')

// incrementing in while loop

console.log(1)
console.log(2)
console.log(3)
console.log(4)
console.log(5)
console.log('Counting completed!')

var i = 1
while(i <= 5){
    console.log(i);
    i++
}
console.log('Counting completed!')

// decrementing on while loop

console.log(5)
console.log(4)
console.log(3)
console.log(2)
console.log(1)
console.log('Countdown finished!')

var i = 5
while(i > 0){
    console.log(i);
    i--
}
console.log('Countdown finished!')


